package vo;

public class Categorys {
	
	private int categorysNo;
	private String categorysName;
	
	public Categorys() {}

	public int getCategorysNo() {
		return categorysNo;
	}

	public void setCategorysNo(int categorysNo) {
		this.categorysNo = categorysNo;
	}

	public String getCategorysName() {
		return categorysName;
	}

	public void setCategorysName(String categorysName) {
		this.categorysName = categorysName;
	}

	
	
}
